class Units:
    um = 1e-6
    nm = 1e-3 * um
    mm = 1e3 * um
    m = 1e6 * um
    inch = 0.0254 * m

    Ampere = 1

    second = 1
    s = second
    ms = 1e-3 * second
    us = 1e-6 * second
    ns = 1e-9 * second
    ps = 1e-12 * second
    fs = 1e-15 * second

    Hz = 1 / second
    kHz = 1e3 * Hz
    MHz = 1e6 * Hz
    GHz = 1e9 * Hz
    THz = 1e12 * Hz

    kg = 1
    Volt = 1
    eV = 1.6e-19 * Volt
    meV = eV * 1e-3

    F = 1
    pF = 1e-12 * F
    nF = 1e-9 * F